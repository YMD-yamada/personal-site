import { apps, siteConfig } from '@/config/site';

export function privacyBody(appSlug?: string) {
  const app = apps.find((a) => a.slug === appSlug);
  const appSection = app
    ? `

## 本アプリ（${app.name}）固有の取扱い

本アプリでは、共通方針に加え、次の情報を取り扱うことがあります。

${app.dataCollected.map((item) => `- ${item}`).join('\n')}
`
    : '';

  return `# プライバシーポリシー

最終更新: ${siteConfig.updatedAt}
運営: ${siteConfig.operatorName}（${siteConfig.brandName}）
お問い合わせ: ${siteConfig.supportEmail}

${siteConfig.brandName} および当方が提供するアプリケーション（以下「本サービス」）における個人情報の取扱いを定めます。複数アプリで本ポリシーを共通利用します。アプリ固有の収集項目がある場合は、各アプリページまたは本ページ末尾の追記を参照してください。

## 1. 取得する情報

サービス内容に応じて、次の情報を取得することがあります。

- アカウント登録時のメールアドレスおよび認証情報（ログイン機能を使う場合）
- お問い合わせ時に送信される氏名・メール・内容
- 端末情報、クラッシュログ、利用状況（分析・監視ツールを有効にした場合）
- 決済に必要な情報（有料機能を有効にし、各決済事業者が処理する場合。カード番号等は当方が直接保持しないことがあります）

## 2. 利用目的

- 本サービスの提供、認証、カスタマーサポート
- 障害対応、セキュリティ確保、サービス改善
- 法令に基づく対応、利用規約違反への対応

## 3. 第三者提供・委託

法令に基づく場合を除き、本人の同意なく第三者へ提供しません。インフラ・分析・決済・ストア配信のため、次のような処理委託先を利用することがあります。

- Apple / Google（ストア配信・課金）
- ホスティング・BaaS・監視・決済事業者（例: Vercel、Supabase、Sentry、Stripe）

## 4. 保管・安全管理

適切なアクセス制御のもと保管し、目的達成後または法令の保存期間経過後に削除・匿名化します。

## 5. 開示・訂正・削除

アプリ内のアカウント削除機能、または ${siteConfig.supportEmail} への連絡により請求できます。

## 6. クッキー等（Web）

Web 版では、セッション維持やアクセス解析のため Cookie 等を用いることがあります。

## 7. 改定

改定時は本ページの更新日を改め、必要に応じてアプリ内でも告知します。
${appSection}`;
}

export function termsBody() {
  return `# 利用規約

最終更新: ${siteConfig.updatedAt}
運営: ${siteConfig.operatorName}（${siteConfig.brandName}）

本利用規約は、${siteConfig.brandName} が提供するアプリケーションおよび関連 Web サイト（以下「本サービス」）の利用条件を定めます。複数アプリで共通適用します。

## 1. 同意

本サービスを利用した時点で、本規約に同意したものとみなします。

## 2. アカウント

ログインはサービスにより任意または必須です。登録情報は正確に保ち、認証情報を第三者に貸与しないでください。

## 3. 禁止事項

法令違反、不正アクセス、他者への迷惑、過度な負荷、リバースエンジニアリング、運営の妨害を禁止します。

## 4. 課金・デジタルコンテンツ

- アプリ内のデジタルコンテンツ・機能解放は、原則として各ストアのアプリ内課金（IAP）に従います。
- Web 上の課金や、実物・役務の対価は Stripe 等の外部決済を用いることがあります。
- 返金は各決済事業者・ストアポリシーおよび適用法令に従います。
- 課金機能が無効のアプリでは本条は将来の有効化に備えた規定です。

## 5. 知的財産

本サービスに関する権利は運営または正当な権利者に帰属します。

## 6. 免責

本サービスは現状有姿で提供されます。合理的な範囲で安定稼働に努めますが、中断・データ損失等について、法令で認められる範囲で責任を限定します。

## 7. 準拠法

日本法を準拠法とします。

## 8. お問い合わせ

${siteConfig.supportEmail}`;
}

